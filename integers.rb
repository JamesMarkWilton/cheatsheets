# ==== Integer ===
#

# + Adds two numbers together
1 + 2 # => 3

# - Subtracts one number from the other
1 - 2 # =>  1

# < Check to see if one int is less than the other
1 < 2 = True

# == See if one is equal to the other
3 == 3 # => True

# > See if left is greater than right
3 > 3 # => False

# <= See if left is less than or equal to right
3 <= 3 # => True

# >= See if the left is greater than or equal to the right
3 >= 3 # => True

# to_f Takes an int and makes it a float
3.to_f # => 3.0
